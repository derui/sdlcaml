    SDL_RENDER_TARGETS_RESET
  | SDL_FIRSTEVENT
  | SDL_DROPFILE
  | SDL_CLIPBOARDUPDATE
  | SDL_MULTIGESTURE
  | SDL_DOLLARRECORD
  | SDL_DOLLARGESTURE
  | SDL_FINGERMOTION
  | SDL_FINGERUP
  | SDL_FINGERDOWN
  | SDL_CONTROLLERDEVICEREMAPPED
  | SDL_CONTROLLERDEVICEREMOVED
  | SDL_CONTROLLERDEVICEADDED
  | SDL_CONTROLLERBUTTONUP
  | SDL_CONTROLLERBUTTONDOWN
  | SDL_CONTROLLERAXISMOTION
  | SDL_JOYDEVICEREMOVED
  | SDL_JOYDEVICEADDED
  | SDL_JOYBUTTONUP
  | SDL_JOYBUTTONDOWN
  | SDL_JOYHATMOTION
  | SDL_JOYBALLMOTION
  | SDL_JOYAXISMOTION
  | SDL_MOUSEWHEEL
