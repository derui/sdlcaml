let of_int c =
  try
    match char_of_int c with
    | '\r' -> SDLK_RETURN
    | '\027' -> SDLK_ESCAPE
    | '\b' -> SDLK_BACKSPACE
    | '\t' -> SDLK_TAB
    | ' ' -> SDLK_SPACE
    | '!' -> SDLK_EXCLAIM
    | '"' -> SDLK_QUOTEDBL
    | '#' -> SDLK_HASH
    | '%' -> SDLK_PERCENT
    | '$' -> SDLK_DOLLAR
    | '&' -> SDLK_AMPERSAND
    | '\'' -> SDLK_QUOTE
    | '(' -> SDLK_LEFTPAREN
    | ')' -> SDLK_RIGHTPAREN
    | '*' -> SDLK_ASTERISK
    | '+' -> SDLK_PLUS
    | ',' -> SDLK_COMMA
    | '-' -> SDLK_MINUS
    | '.' -> SDLK_PERIOD
    | '/' -> SDLK_SLASH
    | '0' -> SDLK_0
    | '1' -> SDLK_1
    | '2' -> SDLK_2
    | '3' -> SDLK_3
    | '4' -> SDLK_4
    | '5' -> SDLK_5
    | '6' -> SDLK_6
    | '7' -> SDLK_7
    | '8' -> SDLK_8
    | '9' -> SDLK_9
    | ':' -> SDLK_COLON
    | ';' -> SDLK_SEMICOLON
    | '<' -> SDLK_LESS
    | '=' -> SDLK_EQUALS
    | '>' -> SDLK_GREATER
    | '?' -> SDLK_QUESTION
    | '@' -> SDLK_AT
    | '[' -> SDLK_LEFTBRACKET
    | '\\' -> SDLK_BACKSLASH
    | ']' -> SDLK_RIGHTBRACKET
    | '^' -> SDLK_CARET
    | '_' -> SDLK_UNDERSCORE
    | '`' -> SDLK_BACKQUOTE
    | 'a' -> SDLK_a
    | 'b' -> SDLK_b
    | 'c' -> SDLK_c
    | 'd' -> SDLK_d
    | 'e' -> SDLK_e
    | 'f' -> SDLK_f
    | 'g' -> SDLK_g
    | 'h' -> SDLK_h
    | 'i' -> SDLK_i
    | 'j' -> SDLK_j
    | 'k' -> SDLK_k
    | 'l' -> SDLK_l
    | 'm' -> SDLK_m
    | 'n' -> SDLK_n
    | 'o' -> SDLK_o
    | 'p' -> SDLK_p
    | 'q' -> SDLK_q
    | 'r' -> SDLK_r
    | 's' -> SDLK_s
    | 't' -> SDLK_t
    | 'u' -> SDLK_u
    | 'v' -> SDLK_v
    | 'w' -> SDLK_w
    | 'x' -> SDLK_x
    | 'y' -> SDLK_y
    | 'z' -> SDLK_z
    | '\177' -> SDLK_DELETE
    | _ -> let code = unmask_key_to_scan c in
           scan_to_key (Sdl_scancode.of_int code)
  with Invalid_argument s ->
    let code = unmask_key_to_scan c in
    scan_to_key (Sdl_scancode.of_int code)
